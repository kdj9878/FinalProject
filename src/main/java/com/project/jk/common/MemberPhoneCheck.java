package com.project.jk.common;

public class MemberPhoneCheck {
	private int m_p_no;
	private String m_p_number;
	private String m_p_id;
	private String m_p_successInfo;
	
	
	public MemberPhoneCheck() {
	}


	public MemberPhoneCheck(int m_p_no, String m_p_number, String m_p_id, String m_p_successInfo) {
		super();
		this.m_p_no = m_p_no;
		this.m_p_number = m_p_number;
		this.m_p_id = m_p_id;
		this.m_p_successInfo = m_p_successInfo;
	}


	public int getM_p_no() {
		return m_p_no;
	}


	public void setM_p_no(int m_p_no) {
		this.m_p_no = m_p_no;
	}


	public String getM_p_number() {
		return m_p_number;
	}


	public void setM_p_number(String m_p_number) {
		this.m_p_number = m_p_number;
	}


	public String getM_p_id() {
		return m_p_id;
	}


	public void setM_p_id(String m_p_id) {
		this.m_p_id = m_p_id;
	}


	public String getM_p_successInfo() {
		return m_p_successInfo;
	}


	public void setM_p_successInfo(String m_p_successInfo) {
		this.m_p_successInfo = m_p_successInfo;
	}
	
	
	
	
	
	
}
